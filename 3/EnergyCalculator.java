// Клас для знаходження рішення задачі
public class EnergyCalculator {
    // Метод для знаходження найбільшого значення енергії
    public static double calculateMaxEnergy(double mass, double height) {
        // Тут реалізуємо логіку знаходження максимальної енергії
        return mass * height * 9.8; // Простий приклад, де максимальна енергія - маса * висота * прискорення вільного падіння
    }
}
